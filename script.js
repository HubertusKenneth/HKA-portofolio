// Type writer effect for hero title
function typeWriter(element, text, speed = 100) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    }
    
    type();
}

// Initialize typewriter effect on page load
window.addEventListener('load', () => {
    const heroTitle = document.querySelector('.hero-title');
    if (heroTitle) {
        // Get the text content properly, preserving the span but getting clean text
        const textContent = "Hi, I'm Hubertus Kenneth";
        const nameSpan = '<span class="text-gradient">Hubertus Kenneth</span>';
        const finalText = "Hi, I'm " + nameSpan;
        
        // Clear the content first
        heroTitle.innerHTML = '';
        
        // Type the text without HTML first
        typeWriter(heroTitle, textContent, 50);
        
        // After typing is complete, replace with styled version
        setTimeout(() => {
            heroTitle.innerHTML = finalText;
        }, textContent.length * 50 + 500);
    }
});

// Email copy functionality
